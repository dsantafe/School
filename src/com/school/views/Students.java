    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {

            String mensaje = "";

            if (txtCode.getText().trim().equals("")) {
                mensaje += "Ingrese el c贸digo, ";
            }
            if (txtLastName.getText().trim().equals("")) {
                mensaje += "Ingrese los apellidos, ";
            }
            if (txtFirstMidName.getText().trim().equals("")) {
                mensaje += "Ingrese los nombres, ";
            }
            if (txtEnrollmentDate.getText().trim().equals("")) {
                mensaje += "Ingrese la fecha de ingreso, ";
            }

            if (!mensaje.equals("")) {
                throw new Exception(mensaje.substring(0, mensaje.length() - 2));
            }

            StudentService studentService = new StudentService();

            int id = Integer.valueOf(lblIdModify.getText());
            int code = Integer.valueOf(txtCode.getText());

            studentService.Update(id,
                    txtLastName.getText(),
                    txtFirstMidName.getText(),
                    txtEnrollmentDate.getText(),
                    code);

            btnSearchActionPerformed(evt);
            JOptionPane.showMessageDialog(rootPane, "Se realizo proceso con exito.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

