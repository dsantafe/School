    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        try {

            List<Student> students = new ArrayList<>();
            StudentService studentService = new StudentService();

            if (txtCode.getText().isEmpty()) {
                students = studentService.GetAll();
            } else {
                int code = Integer.parseInt(txtCode.getText());

                Student student =  new Student();
                student = studentService.GetById(code);
                
                if (student.getId() != 0) {
                    students.add(studentService.GetById(code));
                }
            }

            String[] columns = {"Id", "Apellidos", "Nombres", "Fecha", "C贸digo"};
            DefaultTableModel model = new DefaultTableModel(null, columns);

            String[] rows = new String[5];

            for (Student item : students) {
                rows[0] = String.valueOf(item.getId());
                rows[1] = item.getLastName();
                rows[2] = item.getFirstMidName();
                rows[3] = String.valueOf(item.getEnrollmentDate());
                rows[4] = String.valueOf(item.getCode());

                model.addRow(rows);
            }

            if (students.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "No se encontraron registros.");
                tbData.setModel(new DefaultTableModel());
            } else {
                tbData.setModel(model);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

        try {

            String message = "";

            if (txtCode.getText().isEmpty()) {
                message += "Ingrese el c贸digo, ";
            }
            if (txtLastName.getText().isEmpty()) {
                message += "Ingrese los apellidos, ";
            }
            if (txtFirstMidName.getText().isEmpty()) {
                message += "Ingrese los nombres, ";
            }
            if (txtEnrollmentDate.getText().isEmpty()) {
                message += "Ingrese la fecha de ingreso, ";
            }

            if (!message.isEmpty()) {
                throw new Exception(message.substring(0, message.length() - 2));
            }

            StudentService studentService = new StudentService();
            int code = Integer.valueOf(txtCode.getText());

            studentService.Create(txtLastName.getText(),
                    txtFirstMidName.getText(),
                    txtEnrollmentDate.getText(),
                    code);

            btnSearchActionPerformed(evt);
            JOptionPane.showMessageDialog(rootPane, "Se realizo proceso con exito.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }

    }//GEN-LAST:event_btnCreateActionPerformed

    private void tbDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataMouseClicked
        try {
            if (evt.getButton() == 1) {
                int row = tbData.getSelectedRow();

                Student student = new Student();
                StudentService studentService = new StudentService();

                student = studentService.GetById(Integer.valueOf(tbData.getValueAt(row, 4).toString()));

                lblIdModify.setText(String.valueOf(student.getId()));
                txtCode.setText(String.valueOf(student.getCode()));
                txtLastName.setText(student.getLastName());
                txtFirstMidName.setText(student.getFirstMidName());
                txtEnrollmentDate.setText(String.valueOf(student.getEnrollmentDate()));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_tbDataMouseClicked

